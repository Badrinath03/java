public class Operators{
    

        public void display()
        {
   
      int a = 10, b = 5;
      System.out.println(a == b);
      System.out.println(a < b);
      System.out.println(a <= b);
      System.out.println(a > b);
      System.out.println(a >= b);
      System.out.println(a != b);

          }
         
          public static void main(string[] args)
          {
      
                  operators obj = new operators();
                  obj.display();
           }
}
